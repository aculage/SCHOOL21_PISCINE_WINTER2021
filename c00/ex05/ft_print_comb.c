#include <unistd.h>

void	ft_print_comb(void)
{
	char buf[3];
	char put[2];

	put[0] = ',';
	put[1] = ' ';
	buf[0] = '0';
	while (buf[0] <= '7')
	{
		buf[1] = buf[0] + 1;
		while (buf[1] <= '8')
		{
			buf[2] = buf[1] + 1;
			while (buf[2] <= '9')
			{
				write(1, buf, 3);
				if (buf[0] != '7' || buf[1] != '8' || buf[2] != '9')
				{
					write(1, put, 2);
				}
				buf[2]++;
			}
			buf[1]++;
		}
		buf[0]++;
	}
}
