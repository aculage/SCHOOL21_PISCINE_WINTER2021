#include <stddef.h>

int		ft_any(char **tab, int (*f)(char*))
{
	int cnt;

	cnt = 0;
	while (*(tab + cnt) != NULL)
		if (f(*(tab + cnt++)) != 0)
			return (1);
	return (0);
}
